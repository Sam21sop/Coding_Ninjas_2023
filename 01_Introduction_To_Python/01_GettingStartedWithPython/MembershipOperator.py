'''
Python Logical Operator:
      Description                   ---->     operator
1.  Return true if the specified    ---->       in
    value present in the object.

2.  Return True if the specified    ---->       not in
    value not present in the object.
'''

string = 'aeiou'
s = 'a'

#in operator 
print(s in string)

#not in operator
print(s not in string)