'''
python hasfive basic data type
   DataType      Build-in Method
1. integer  --->     (int())
2. float    --->     (float())
3. string   --->     (str())
4. complex  --->     (complex())
5. boolean  --->     (bool())
'''

#int data type 
integer = 99
print(integer, type(integer))

#float data type
flt = 99.99
print(flt, type(flt))

#string data type 
string = 'Python'
print(string, type(string))

#complex data type
complex_type = 3 + 4j
print(complex_type, type(complex_type))

#boolean data type
boolean_true = True 
print(boolean_true, type(boolean_true))

boolean_false = False
print(boolean_false, type(boolean_false))
