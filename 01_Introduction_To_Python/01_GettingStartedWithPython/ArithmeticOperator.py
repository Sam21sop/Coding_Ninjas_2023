'''
Python Arithmetic Operator:
    Name            ---->       operator
1. Addition         ---->        +
2. Subtraction      ---->        -
3. Multiplication   ---->        *
4. Division         ---->        /
5. Modulus          ---->        %
6. Floor division   ---->        //
7. Exponential      ---->        **
'''

#addition operator
addition = 5 + 10
print(addition)

#subtraction operator
subtraction = 10 - 5
print(subtraction)

#multiplication operator
multiplication = 10 * 5
print(multiplication)

#division operator
division = 10 / 5
print(division)

#modulus operator
modulus = 7 % 5
print(modulus)

#floor division
floor_division = 13 // 6
print(floor_division)

#exponential operator
expon = 10 ** 5
print(expon)