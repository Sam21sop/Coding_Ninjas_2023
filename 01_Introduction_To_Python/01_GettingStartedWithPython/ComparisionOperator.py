'''
Python Comparision Operator:
    Name                ---->       operator
1. Equal To             ---->           ==
2. Not equal            ---->           !=
3. Greater Than         ---->           >
4. Greater or Equal to  ---->           >=
5. Less Than            ---->           < 
6. Less or Equal To     ---->           <=
'''
x = 10
y = 20

#equal to
print(x == y)

#not equal to
print(x != y)

#greater than
print(x > y)

#greater or equal to
print(x >= y)

#less than
print(x < y)

#less than
print(x <= y)